// For loop

for (let i = 1; i <= 5; i++) {
  console.log("Count:", i);
}

const fruits = ["Apple", "Banana", "Mango"];

for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}

const numbers = [10, 20, 30, 40];

let total = 0;

for (let i = 0; i < numbers.length; i++) {
  total = total + numbers[i];
}

console.log("Total:", total);
